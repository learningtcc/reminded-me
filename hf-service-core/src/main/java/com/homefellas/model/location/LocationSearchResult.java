package com.homefellas.model.location;

import com.homefellas.model.core.ICacheable;

public class LocationSearchResult implements ICacheable
{

	private String defaultZip;
	private String display;
	
	public String getDefaultZip()
	{
		return defaultZip;
	}
	public void setDefaultZip(String defaultZip)
	{
		this.defaultZip = defaultZip;
	}
	public String getDisplay()
	{
		return display;
	}
	public void setDisplay(String display)
	{
		this.display = display;
	}
	
	
}
